public class FloatDemo{ 
    public static void main(String[] args) {
        double PI = 3.14159;
        System.out.println("El valor de PI aproximadamente es: " + PI);

        double Radius = 5.0;
        double Area = PI * Radius * Radius;
        System.out.println("El Radio de las circunferencia es: " + Radius);
        System.out.println("El Area del circulo es: " + Area);
                
    }
}